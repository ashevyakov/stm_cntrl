	uint8_t dir2 = (data21 < 0) ? GPIO_PIN_SET : GPIO_PIN_RESET;
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2, dir1);
	  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_3, dir2);
	data11 = abs(data11);
	data21 = abs(data21);

